export const PIECES = Object.keys(SHAPES) as PieceType[];
export const PIECE_ID: Record<PieceType, 1 | 2 | 3 | 4 | 5 | 6 | 7> = { I: 1, O: 2, T: 3, S: 4, Z: 5, J: 6, L: 7 };

export function cells(type: PieceType, rotation: number): readonly Point[] {
  return SHAPES[type][((rotation % 4) + 4) % 4] ?? SHAPES[type][0]!;
}

export function kicks(type: PieceType): readonly Point[] {
  return type === "I"
    ? [[0, 0], [-2, 0], [2, 0], [-1, 0], [1, 0], [0, -1], [0, 1]]
    : [[0, 0], [-1, 0], [1, 0], [-2, 0], [2, 0], [0, -1]];
}
